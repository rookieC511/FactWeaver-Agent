def _generate_filler_text(target_chars: int, seed: int = 42) -> str:
    """生成约 target_chars 字符的中文科技白噪音文本"""
    rng = random.Random(seed)
    paragraphs = []
    total = 0
    while total < target_chars:
        p = rng.choice(_FILLER_PARAGRAPHS)
        paragraphs.append(p)
        total += len(p) + 2  # +2 for \n\n
    text = "\n\n".join(paragraphs)
    return text[:target_chars]


def _insert_needle(filler: str, needle: str, position: str) -> str:
    """在 filler 文本的 middle 或 tail 位置插入 needle"""
    if position == "middle":
        mid = len(filler) // 2
        # 找到 mid 附近的段落分隔符
        split_pos = filler.rfind("\n\n", 0, mid)
        if split_pos == -1:
            split_pos = mid
        return filler[:split_pos] + "\n\n" + needle + "\n\n" + filler[split_pos:]
    elif position == "tail":
        # 在倒数第 2000 字符处插入
        tail_pos = max(0, len(filler) - 2000)
        split_pos = filler.rfind("\n\n", 0, tail_pos)
        if split_pos == -1:
            split_pos = tail_pos
        return filler[:split_pos] + "\n\n" + needle + "\n\n" + filler[split_pos:]
    else:
        return filler + "\n\n" + needle


