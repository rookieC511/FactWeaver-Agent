def build_test_case(case: dict) -> dict:
    """构建完整测试用例：生成白噪音填充 + 植入 needle"""
    TARGET_CHARS = 20000
    filler = _generate_filler_text(TARGET_CHARS, seed=case["seed"])
    full_text = _insert_needle(filler, case["needle"], case.get("needle_position", "middle"))
    return {
        **case,
        "text": full_text,
        "text_length": len(full_text),
    }


def get_all_cases() -> list:
    """返回所有 5 个完整测试用例"""
    return [build_test_case(c) for c in TEST_CASES]


